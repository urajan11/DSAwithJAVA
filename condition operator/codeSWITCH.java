import java.util.*;
public class codeSWITCH {
    public static void main(String args[]){
     Scanner sc=new Scanner(System.in);
     int num=sc.nextInt();// enter no foe switch case
    
     switch(num){
        case 1: System.out.println("Rajan");
        break;
        case 2:System.out.println("Tannu");
        break;
        case 3:System.out.println("cool");
        break;
        default:System.out.println("sad");
     }


    }
    
}
